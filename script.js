const header = document.querySelector("[data-header]");
const nav = document.querySelector("[data-nav]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const languageButtons = document.querySelectorAll("[data-lang]");
const year = document.querySelector("[data-year]");
const metaDescription = document.querySelector('meta[name="description"]');
const budgetRange = document.querySelector("[data-budget-range]");
const budgetOutput = document.querySelector("[data-budget-output]");
const budgetValueInput = document.querySelector("[data-budget-value-input]");
const contactForm = document.querySelector(".contact-form");
const customSelects = document.querySelectorAll("[data-custom-select]");
const reduceMotionQuery = window.matchMedia("(prefers-reduced-motion: reduce)");

function setHeaderState() {
  header.classList.toggle("scrolled", window.scrollY > 8);
}

function closeMenu() {
  document.body.classList.remove("nav-open");
  nav.classList.remove("open");
  header.classList.remove("menu-active");
  menuToggle.setAttribute("aria-expanded", "false");
}

function initSmoothScroll() {
  const supportsFinePointer = window.matchMedia("(pointer: fine)").matches;
  if (window.innerWidth <= 760) return; // Disable Lenis on mobile devices

  if (reduceMotionQuery.matches || !supportsFinePointer || typeof window.Lenis !== "function") {
    return;
  }

  window.atelaScroll = new window.Lenis({
    anchors: {
      duration: 0.8
    },
    autoRaf: true,
    duration: 0.72,
    easing: (progress) => 1 - Math.pow(1 - progress, 4),
    smoothWheel: true,
    stopInertiaOnNavigate: true,
    syncTouch: false,
    wheelMultiplier: 0.95
  });
}

function updateBudgetLabel(language = document.documentElement.lang || "en") {
  if (!budgetRange || !budgetOutput || !budgetValueInput) {
    return;
  }

  const locale = language === "es" ? "es-ES" : "en-US";
  const amount = Number(budgetRange.value);
  const label = new Intl.NumberFormat(locale, {
    style: "currency",
    currency: "EUR",
    maximumFractionDigits: 0
  })
    .format(amount)
    .replace(/\s/g, " ");

  budgetOutput.textContent = label;
  budgetValueInput.value = label;
}

function updateCustomSelectLabels() {
  customSelects.forEach((select) => {
    const type = select.dataset.customSelect;
    const current = document.querySelector(`[data-custom-current="${type}"]`);
    const options = document.querySelectorAll(`[data-custom-option="${type}"]`);
    
    if (!current) return;
    
    const selectedOption = Array.from(options).find((opt) => opt.checked);
    
    if (!selectedOption) {
      const dictionary = translations[document.documentElement.lang] || translations.en;
      const placeholderKey = type === "interest" ? "formInterestPlaceholder" : "formResidencePlaceholder";
      current.textContent = dictionary[placeholderKey];
      return;
    }
    
    const label = selectedOption.closest("label")?.querySelector("span");
    current.textContent = label?.textContent || selectedOption.value;
  });
}

function initRevealMotion() {
  const imageMotionElements = document.querySelectorAll(".figure-card, .zone-photo-grid article, .about-image");
  const revealElements = document.querySelectorAll(
    ".intro, .figure-card, .areas-heading, .map-panel, .zone-photo-grid article, .service-chapter, .legal-layout, .availability-heading, .routes-table, .about-image, .about-copy, .contact-heading, .contact-form, .site-footer"
  );

  imageMotionElements.forEach((element) => {
    element.classList.add("image-reveal", "image-drift");
  });

  if (reduceMotionQuery.matches || !("IntersectionObserver" in window)) {
    revealElements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  revealElements.forEach((element, index) => {
    element.classList.add("reveal");
    element.style.setProperty("--reveal-delay", `${(index % 3) * 70}ms`);
  });

  const revealObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      rootMargin: "0px 0px -12% 0px",
      threshold: 0.16
    }
  );

  revealElements.forEach((element) => revealObserver.observe(element));

  const driftElements = Array.from(imageMotionElements);
  let driftFrame = null;

  function updateImageDrift() {
    driftFrame = null;

    driftElements.forEach((element) => {
      const image = element.querySelector("img");
      if (!image) {
        return;
      }

      const rect = element.getBoundingClientRect();
      const progress = (window.innerHeight - rect.top) / (window.innerHeight + rect.height);
      const clampedProgress = Math.min(1, Math.max(0, progress));
      const shift = (clampedProgress - 0.5) * 18;
      image.style.setProperty("--image-shift", `${shift.toFixed(2)}px`);
    });
  }

  function requestImageDrift() {
    if (driftFrame !== null) {
      return;
    }

    driftFrame = window.requestAnimationFrame(updateImageDrift);
  }

  updateImageDrift();
  window.addEventListener("scroll", requestImageDrift, { passive: true });
  window.addEventListener("resize", requestImageDrift);
}

function setLanguage(language) {
  const dictionary = translations[language] || translations.es;

  document.documentElement.lang = language;
  document.title = dictionary.pageTitle;
  metaDescription.setAttribute("content", dictionary.pageDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("atela-language", language);
  updateBudgetLabel(language);
  updateCustomSelectLabels();
}

menuToggle.addEventListener("click", () => {
  const isOpen = nav.classList.toggle("open");
  document.body.classList.toggle("nav-open", isOpen);
  header.classList.toggle("menu-active", isOpen);
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

nav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", closeMenu);
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

window.addEventListener("scroll", setHeaderState, { passive: true });

if (budgetRange) {
  budgetRange.addEventListener("input", () => updateBudgetLabel());
}

document.querySelectorAll("[data-custom-option]").forEach((option) => {
  option.addEventListener("change", (e) => {
    const type = e.target.dataset.customOption;
    const select = document.querySelector(`[data-custom-select="${type}"]`);
    updateCustomSelectLabels();
    select?.classList.remove("is-invalid");
    select?.removeAttribute("open");
  });
});

document.addEventListener("click", (event) => {
  customSelects.forEach((select) => {
    if (select.open && !select.contains(event.target)) {
      select.removeAttribute("open");
    }
  });
});

contactForm?.addEventListener("submit", (event) => {
  event.preventDefault();
  let hasError = false;
  
  customSelects.forEach((select) => {
    const type = select.dataset.customSelect;
    const options = document.querySelectorAll(`[data-custom-option="${type}"]`);
    const hasSelection = Array.from(options).some((option) => option.checked);
    const isRequired = select.getAttribute("aria-required") === "true";
    
    if (isRequired && !hasSelection) {
      hasError = true;
      select.setAttribute("open", "");
      select.classList.add("is-invalid");
      select.querySelector("summary")?.focus();
    }
  });

  if (hasError) return;

  const fd = new FormData(contactForm);
  const name = fd.get("name") || "";
  const email = fd.get("email") || "";
  const phone = fd.get("phone") || "";
  const location = fd.get("location") || "";
  const interest = fd.get("interest") || "";
  const residence = fd.get("residence_type") || "";
  const budget = fd.get("budget") || "";
  const message = fd.get("message") || "";

  const subject = `Nueva consulta web: ${name}`;
  
  let body = `Nombre: ${name}\n`;
  body += `Email: ${email}\n`;
  if (phone) body += `Teléfono: ${phone}\n`;
  if (location) body += `Ubicación actual: ${location}\n`;
  if (interest) body += `Interés principal: ${interest}\n`;
  if (residence) body += `Tipo de residencia: ${residence}\n`;
  body += `Presupuesto: ${budget}\n\n`;
  if (message) body += `Mensaje:\n${message}\n\n`;
  
  body += `--- \nPrivacidad: Aceptada.\n`;

  const mailtoLink = `mailto:info@atelahomes.com?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailtoLink;

  // Fallback UI for desktop users without a default mail client
  const submitGroup = contactForm.querySelector('.form-submit-group');
  let fallback = document.getElementById('mailto-fallback');
  if (!fallback) {
    fallback = document.createElement('div');
    fallback.id = 'mailto-fallback';
    fallback.style.marginTop = '16px';
    fallback.style.padding = '14px 18px';
    fallback.style.background = 'rgba(247, 243, 236, 0.1)';
    fallback.style.border = '1px solid rgba(247, 243, 236, 0.3)';
    fallback.style.borderRadius = '8px';
    fallback.style.color = 'var(--paper)';
    fallback.style.fontSize = '0.9rem';
    submitGroup.appendChild(fallback);
  }
  
  fallback.innerHTML = currentLang === 'es' 
    ? "Si tu aplicación de correo no se abre automáticamente, <a href='" + mailtoLink + "' style='text-decoration:underline; font-weight:560;'>haz clic aquí</a> o escribe a <b>info@atelahomes.com</b>."
    : "If your mail app doesn't open automatically, <a href='" + mailtoLink + "' style='text-decoration:underline; font-weight:560;'>click here</a> or email <b>info@atelahomes.com</b>.";
});

year.textContent = new Date().getFullYear();
setHeaderState();
initSmoothScroll();
initRevealMotion();
setLanguage(localStorage.getItem("atela-language") || "en");
